#!/bin/bash 

echo "
*************************************************************************************
*************************************************************************************
********************************Test de la config reseau sur www.google.fr***********
*************************************************************************************
*************************************************************************************
"
ping -c 3 www.google.fr > /dev/null

if [[ $? -eq 0 ]]; then
    echo "
*************************************************************************************
********************************Config reseau OK !***********************************
*************************************************************************************
"
else
    echo "
*************************************************************************************
********************************Erreur dans la config reseau*************************
*************************************************************************************
"
    exit 1
fi


echo "
*************************************************************************************
*************************************************************************************
********************************Installation de Apache2 Server***********************
*************************************************************************************
*************************************************************************************
"

apt update -y > /dev/null

apt install -y apache2 php php-mysqli php-xml php-curl php-mbstring php-gd php-intl php-imap php-zip mariadb-server

systemctl restart apache2

if [[ $? -eq 0 ]]; then
    echo "
*************************************************************************************
*************************************************************************************
********************************Installation reussite********************************
*************************************************************************************
*************************************************************************************

******************************************************
Connectez-vous Ã  cette IP sur le poste client : ******
******************************************************

$(ip a)

***********************************************
Le chemin de vos pages web est /var/www *******
***********************************************
"
else
    echo "
*************************************************************************************
*************************************************************************************
********************************Erreur lors de l'installation************************
*************************************************************************************
*************************************************************************************
"
fi
