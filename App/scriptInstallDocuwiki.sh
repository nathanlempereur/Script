#!/bin/bash 

echo "
*************************************************************************************
*************************************************************************************
********************************Installation de Apache2 Server***********************
*************************************************************************************
*************************************************************************************
"

./scriptInstallApache2.sh


echo "
*************************************************************************************
*************************************************************************************
********************************Recuperation du Dokuwiki****************************
*************************************************************************************
*************************************************************************************
"
apt install -y wget zip

wget https://www.pedagogeek.fr/cours/vm/WebApps/dokuwiki-2024-02-06b.tgz

tar -xzf dokuwiki-2024-02-06b.tgz

rm dokuwiki-2024-02-06b.tgz

mv dokuwiki /var/www/

if [[ $? -eq 0 ]]; then
    echo "
*************************************************************************************
********************************Extraction vers /var/www/ OK !***********************
*************************************************************************************
"
else
    echo "
*************************************************************************************
********************************Erreur dans l extraction*****************************
*************************************************************************************
"
    exit 0
fi


echo "
*************************************************************************************
*************************************************************************************
********************************Configuration du site********************************
*************************************************************************************
*************************************************************************************
"

a2dissite 000-default.conf

echo "
<VirtualHost *:80>
	ServerAdmin webmaster@localhost
	DocumentRoot /var/www/dokuwiki
	ErrorLog ${APACHE_LOG_DIR}/error.log
	CustomLog ${APACHE_LOG_DIR}/access.log combined
	
	<Directory /var/www/dokuwiki>
		Options Indexes FollowSymLinks
		Require all granted
	</Directory>
</VirtualHost>" > /etc/apache2/sites-available/dokuwiki.conf

a2ensite dokuwiki.conf

chown -R www-data:www-data /var/www/dokuwiki

systemctl restart apache2


if [[ $? -eq 0 ]]; then
    echo "
*************************************************************************************
*************************************************************************************
********************************Installation reussite********************************
*************************************************************************************
*************************************************************************************

******************************************************
Connectez-vous Ã  cette IP sur le poste client : ******
******************************************************

$(ip a)

**********************************************************
Le chemin de votre page web est /var/www/dokuwiki ********
**********************************************************
"
else
    echo "
*************************************************************************************
*************************************************************************************
********************************Erreur lors de l'installation************************
*************************************************************************************
*************************************************************************************
"
fi
