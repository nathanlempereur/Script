#!/bin/bash 

echo "
*************************************************************************************
*************************************************************************************
********************************Installation de Apache2 Server***********************
*************************************************************************************
*************************************************************************************
"

./scriptInstallApache2.sh


echo "
*************************************************************************************
*************************************************************************************
********************************Recuperation du GLPI*********************************
*************************************************************************************
*************************************************************************************
"
apt install -y wget

wget https://www.pedagogeek.fr/cours/vm/WebApps/glpi-10.0.17.tgz

tar -xzf glpi-10.0.17.tgz

mv glpi /var/www/

rm glpi-10.0.17.tgz


if [[ $? -eq 0 ]]; then
    echo "
*************************************************************************************
********************************Extraction vers /var/www/ OK !***********************
*************************************************************************************
"
else
    echo "
*************************************************************************************
********************************Erreur dans l extraction*****************************
*************************************************************************************
"
    exit 0
fi


echo "
*************************************************************************************
*************************************************************************************
********************************Configuration du site********************************
*************************************************************************************
*************************************************************************************
"

a2dissite 000-default.conf

echo "
<VirtualHost *:80>
	ServerAdmin webmaster@localhost
	DocumentRoot /var/www/glpi
	ErrorLog ${APACHE_LOG_DIR}/error.log
	CustomLog ${APACHE_LOG_DIR}/access.log combined
	
	<Directory /var/www/glpi>
		Options Indexes FollowSymLinks
		Require all granted
	</Directory>
</VirtualHost>" > /etc/apache2/sites-available/glpi.conf

a2ensite glpi.conf

chown -R www-data:www-data /var/www/glpi

mysql -u root -e "
CREATE DATABASE glpi;
GRANT ALL PRIVILEGES ON glpi.* TO 'GLPI1'@'localhost' IDENTIFIED BY 'btsinfo';
FLUSH PRIVILEGES;
"

systemctl restart apache2


if [[ $? -eq 0 ]]; then
    echo "
*************************************************************************************
*************************************************************************************
********************************Installation reussite********************************
*************************************************************************************
*************************************************************************************

******************************************************
Connectez-vous Ã  cette IP sur le poste client : ******
******************************************************

$(ip a)

**********************************************************
Le chemin de votre page web est /var/www/glpi ************
**********************************************************
Nom de la BDD   : glpi      ******************************
Nom du clients  : GLPI1     ******************************
MDP             : btsinfo   ******************************
**********************************************************
"
else
    echo "
*************************************************************************************
*************************************************************************************
********************************Erreur lors de l'installation************************
*************************************************************************************
*************************************************************************************
"
fi
