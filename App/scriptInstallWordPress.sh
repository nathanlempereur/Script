#!/bin/bash 

echo "
*************************************************************************************
*************************************************************************************
********************************Installation de Apache2 Server***********************
*************************************************************************************
*************************************************************************************
"

./scriptInstallApache2.sh


echo "
*************************************************************************************
*************************************************************************************
********************************Recuperation du WordPress****************************
*************************************************************************************
*************************************************************************************
"
apt install -y wget zip

wget https://www.pedagogeek.fr/cours/vm/WebApps/wordpress-6.7.1-fr_FR.zip

unzip wordpress-6.7.1-fr_FR.zip

rm wordpress-6.7.1-fr_FR.zip

mv wordpress /var/www/

if [[ $? -eq 0 ]]; then
    echo "
*************************************************************************************
********************************Extraction vers /var/www/ OK !***********************
*************************************************************************************
"
else
    echo "
*************************************************************************************
********************************Erreur dans l extraction*****************************
*************************************************************************************
"
    exit 0
fi


echo "
*************************************************************************************
*************************************************************************************
********************************Configuration du site********************************
*************************************************************************************
*************************************************************************************
"

a2dissite 000-default.conf

echo "
<VirtualHost *:80>
	ServerAdmin webmaster@localhost
	DocumentRoot /var/www/wordpress
	ErrorLog ${APACHE_LOG_DIR}/error.log
	CustomLog ${APACHE_LOG_DIR}/access.log combined
	
	<Directory /var/www/wordpress>
		Options Indexes FollowSymLinks
		Require all granted
	</Directory>
</VirtualHost>" > /etc/apache2/sites-available/wordpress.conf

a2ensite wordpress.conf

chown -R www-data:www-data /var/www/wordpress

mysql -u root -e "
CREATE DATABASE wordpress;
GRANT ALL PRIVILEGES ON wordpress.* TO 'WP1'@'localhost' IDENTIFIED BY 'btsinfo';
FLUSH PRIVILEGES;
"

systemctl restart apache2


if [[ $? -eq 0 ]]; then
    echo "
*************************************************************************************
*************************************************************************************
********************************Installation reussite********************************
*************************************************************************************
*************************************************************************************

******************************************************
Connectez-vous Ã  cette IP sur le poste client : ******
******************************************************

$(ip a)

**********************************************************
Le chemin de votre page web est /var/www/wordpress *******
**********************************************************
Nom de la BDD   : wordpress ******************************
Nom du clients  : WP1       ******************************
MDP             : btsinfo   ******************************
**********************************************************
"
else
    echo "
*************************************************************************************
*************************************************************************************
********************************Erreur lors de l'installation************************
*************************************************************************************
*************************************************************************************
"
fi
