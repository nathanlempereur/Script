#!/bin/bash 

echo "
*************************************************************************************
*************************************************************************************
********************************Installation de Apache2 Server***********************
*************************************************************************************
*************************************************************************************
"

./scriptInstallApache2.sh


echo "
*************************************************************************************
*************************************************************************************
********************************Recuperation de Drupal*******************************
*************************************************************************************
*************************************************************************************
"
apt install -y wget zip

wget https://www.pedagogeek.fr/cours/vm/WebApps/drupal-10.0.7.tar.gz

tar -xzf drupal-10.0.7.tar.gz

rm drupal-10.0.7.tar.gz

mv drupal-10.0.7 /var/www/

if [[ $? -eq 0 ]]; then
    echo "
*************************************************************************************
********************************Extraction vers /var/www/ OK !***********************
*************************************************************************************
"
else
    echo "
*************************************************************************************
********************************Erreur dans l extraction*****************************
*************************************************************************************
"
    exit 0
fi


echo "
*************************************************************************************
*************************************************************************************
********************************Configuration du site********************************
*************************************************************************************
*************************************************************************************
"

a2dissite 000-default.conf

echo "
<VirtualHost *:80>
	ServerAdmin webmaster@localhost
	DocumentRoot /var/www/drupal-10.0.7
	ErrorLog ${APACHE_LOG_DIR}/error.log
	CustomLog ${APACHE_LOG_DIR}/access.log combined
	
	<Directory /var/www/drupal-10.0.7>
		Options Indexes FollowSymLinks
		Require all granted
	</Directory>
</VirtualHost>" > /etc/apache2/sites-available/drupal.conf

a2ensite drupal.conf

chown -R www-data:www-data /var/www/drupal-10.0.7

mysql -u root -e "
CREATE DATABASE drupal;
GRANT ALL PRIVILEGES ON drupal.* TO 'DPL1'@'localhost' IDENTIFIED BY 'btsinfo';
FLUSH PRIVILEGES;
"

systemctl restart apache2


if [[ $? -eq 0 ]]; then
    echo "
*************************************************************************************
*************************************************************************************
********************************Installation reussite********************************
*************************************************************************************
*************************************************************************************

******************************************************
Connectez-vous Ã  cette IP sur le poste client : ******
******************************************************

$(ip a)

**********************************************************
Le chemin de votre page web est /var/www/drupal-10.0.7 ***
**********************************************************
Nom de la BDD   : drupal  ********************************
Nom du clients  : DPL1    ********************************
MDP             : btsinfo ********************************
**********************************************************
"
else
    echo "
*************************************************************************************
*************************************************************************************
********************************Erreur lors de l'installation************************
*************************************************************************************
*************************************************************************************
"
fi
