#!/bin/bash 

echo "
*************************************************************************************
*************************************************************************************
********************************Installation de Apache2 Server***********************
*************************************************************************************
*************************************************************************************
"

./scriptInstallApache2.sh


echo "
*************************************************************************************
*************************************************************************************
********************************Recuperation du DotClear*****************************
*************************************************************************************
*************************************************************************************
"
apt install -y wget zip

wget https://download.dotclear.org/latest.zip

unzip latest.zip

rm latest.zip

mv dotclear /var/www/

if [[ $? -eq 0 ]]; then
    echo "
*************************************************************************************
********************************Extraction vers /var/www/ OK !***********************
*************************************************************************************
"
else
    echo "
*************************************************************************************
********************************Erreur dans l extraction*****************************
*************************************************************************************
"
    exit 0
fi


echo "
*************************************************************************************
*************************************************************************************
********************************Configuration du site********************************
*************************************************************************************
*************************************************************************************
"

a2dissite 000-default.conf

echo "
<VirtualHost *:80>
	ServerAdmin webmaster@localhost
	DocumentRoot /var/www/dotclear
	ErrorLog ${APACHE_LOG_DIR}/error.log
	CustomLog ${APACHE_LOG_DIR}/access.log combined
	
	<Directory /var/www/dotclear>
		Options Indexes FollowSymLinks
		Require all granted
	</Directory>
</VirtualHost>" > /etc/apache2/sites-available/dotclear.conf

a2ensite dotclear.conf

chown -R www-data:www-data /var/www/dotclear

mysql -u root -e "
CREATE DATABASE dotclear;
GRANT ALL PRIVILEGES ON dotclear.* TO 'DC1'@'localhost' IDENTIFIED BY 'btsinfo';
FLUSH PRIVILEGES;
"

systemctl restart apache2


if [[ $? -eq 0 ]]; then
    echo "
*************************************************************************************
*************************************************************************************
********************************Installation reussite********************************
*************************************************************************************
*************************************************************************************

******************************************************
Connectez-vous Ã  cette IP sur le poste client : ******
******************************************************

$(ip a)

**********************************************************
Le chemin de votre page web est /var/www/dotclear  *******
**********************************************************
Nom de la BDD   : dotclear  ******************************
Nom du clients  : DC1       ******************************
MDP             : btsinfo   ******************************
**********************************************************
"
else
    echo "
*************************************************************************************
*************************************************************************************
********************************Erreur lors de l'installation************************
*************************************************************************************
*************************************************************************************
"
fi
