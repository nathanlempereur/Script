#!/bin/bash

echo "
**************************************************************************
**************************************************************************
***************************Creation de backup d'Apache********************
**************************************************************************
**************************************************************************
"

date_sauvegarde=$(date +"%d-%m-%Y_%H-%M-%S")
nom="archive"
dossier="${nom}_${date_sauvegarde}"
mkdir /root/$dossier
mkdir /root/$dossier/var
cp -R /var/www/  /root/$dossier/var/

mkdir /root/$dossier/etc
cp -R /etc/apache2/ /root/$dossier/etc/

mysqldump WordPress > /root/$dossier/backupWordPress.sql

gzip /root/$dossier

echo "
**************************************************************************
**************************************************************************
***************************Creation du backup fini !**********************
**************************************************************************
**************************************************************************
"
